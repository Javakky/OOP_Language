package com.github.javakky.oopl._7;

public class PracticeBoolBit{
	public static void main(String[] args){
		if(true | false){
			System.out.println("true | false");
		}
		if(true & false){
			System.out.println("true & false");
		}
		if(true ^ true){
			System.out.println("true ^ true");
		}	
	}
}
